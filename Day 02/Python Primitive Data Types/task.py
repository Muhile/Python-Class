# Learn about the primitive data types in python. (Strings, Integers, Floats, Booleans)

#Code to print the first character in the string
print("Hello"[0])

# An integer is a whole number without decimal places.
print(123 + 345)

#Large integers
print(342,354,896)
# or
print(342_354_896)

# A float is a number with decimal places. Floating point number
print(3.14159)

# A boolean is either True or False
print(True)
print(False)